    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        tm=new DefaultTableModel(null, new Object[] {"NISN", "NIS", "Nama", "Jenis Kelamin", "Kelas", "Alamat", "No. HP", "Tarif SPP"});
        jTable1.setModel(tm);
        tm.getDataVector().removeAllElements();
        try{
            PreparedStatement s = con.prepareStatement("SELECT siswa.nisn, siswa.nis,siswa.nama ,siswa.jk, kelas.nama_kelas, "
                    + "siswa.alamat, siswa.no_telp, spp.tahun FROM siswa INNER JOIN kelas USING(id_kelas) INNER JOIN "
                    + "spp USING(id_spp) where nama LIKE '%"+ txtcari.getText().toString()+"%'");
            ResultSet r = s.executeQuery();
            while (r.next()) {
                Object[] data = {
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),
                    r.getString(7),
                    r.getString(8)
                };
                tm.addRow(data);
            }
        }catch (Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "n\n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

