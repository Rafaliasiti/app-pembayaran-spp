    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        if (txtnisn.getText().equals("") || txtnis.getText().equals("") || txtnama.getText().equals("")
            || txtidkelas.getText().equals("") || txtalamat.getText().equals("")   
            || txtnohp.getText().equals("") || txtidspp.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Data harus terisi semua");
        } else { 
        try{
            PreparedStatement ps = con.prepareStatement("INSERT INTO siswa VALUES(?,?,?,?,?,?,?,?)");
            ps.setString(1, txtnisn.getText());
            ps.setString(2, txtnis.getText());
            ps.setString(3, txtnama.getText());
            ps.setString(4, cbjk.getSelectedItem().toString());
            ps.setString(5, txtidkelas.getText());
            ps.setString(6, txtalamat.getText());
            ps.setString(7, txtnohp.getText());
            ps.setString(8, txtidspp.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan");
            refreshTable();            
        }catch (Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n "+e+"\n\n");
        }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

